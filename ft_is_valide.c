#include "fillit.h"

int		ft_is_valide(const char *s, int i)
{
	if ((s[i + 5] == '#' && s[i + 1] == '#' && i < 15)
		|| (s[i + 5] == '#' && s[i - 1] == '#' && i < 15)
		|| (s[i - 5] == '#' && s[i + 1] == '#' && i > 4)
		|| (s[i - 5] == '#' && s[i - 1] == '#' && i > 4)
		|| (s[i + 1] == '#' && s[i + 10] == '#' && i < 10)
		|| (s[i + 1] == '#' && s[i - 10] == '#' && i > 9)
		|| (s[i + 2] == '#' && s[i + 5] == '#' && i < 15)
		|| (s[i - 2] == '#' && s[i + 5] == '#' && i < 15)
		|| (s[i + 2] == '#' && s[i - 5] == '#' && i > 4)
		|| (s[i - 2] == '#' && s[i - 5] == '#' && i > 4))
		return (1);
	else
		return (0);
}
