#include "fillit.h"

int		ft_min_max(char *tetris)
{
	int i;
	int j;
	int k;

	i = 0;
	j = 1;
	k = 0;
	while (tetris[i])
	{
		if (tetris[i] == '\n' && tetris[i + 1] == '\n')
			j++;
		i++;
	}
	j *= 4;
	j = ft_sqrt_map(j);
	return (j);
}
